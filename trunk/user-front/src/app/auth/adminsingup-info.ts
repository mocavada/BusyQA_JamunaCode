export class AdminSignUpInfo {
  name: string;
  username: string;
  email: string;
  roles: string[];
  teams: string[];
  password: string;
  status: string;
  statusAsOfDay: string;

  constructor(name: string, username: string, email: string, password: string, roles: string[], teams: string[]) {
    this.name = name;
    this.username = username;
    this.email = email;
    this.password = password;
    this.roles = roles;
    this.teams = teams;
    this.status = 'YES';
    this.statusAsOfDay = '';
  }
}
